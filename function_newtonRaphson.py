def newtonRaphson(x, f, fp):
    # Calculate and return the next point for NewtonRaphson

    # Special case if f prime = 0
    if fp == 0:
        x_np1 = x + 0.001
        
    else:
        x_np1 = x*1.000 - f/(fp*1.000)

    return x_np1
