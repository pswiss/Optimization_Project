def calcDistance(coord0, coord1):
    # This function takes in two points in arbitrary-dimensional space
    # Calculate the vector from coordinate 1 to coordinate 2
    vector = [a_i - b_i for a_i, b_i in zip(coord0, coord1)]
    # Calculate the length of this vector
    distance = pow(sum([el*el for el in vector]),0.5)

    return distance
