def calcError(Robots):
    # This function will calculate the errors between robot calculated and true position
    errors = []

    for robot in Robots:
        distErr = calcDistance([robot.xTrue, robot.yTrue], [robot.xGuess, robot.yGuess])
        errors.append(distErr)

        
    return errors
